document.addEventListener("scroll", function () {
  var leftColumn = document.querySelector(".box");
  var leftColumnPosition = leftColumn.getBoundingClientRect().top;
  var windowHeight = window.innerHeight;

  // Check if the left column is within the viewport
  if (leftColumnPosition < windowHeight * 0.8) {
    // Adjust the threshold as needed
    leftColumn.classList.add("animate__bounceIn");
  } else {
    leftColumn.classList.remove("animate__bounceIn");
  }
});
